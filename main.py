item_one = "Phone"
item_price = 100
item_quantity = 5
item_total_price = item_price * item_quantity

print(type(item_one))  # <class 'str'>
print(type(item_price))  # <class 'int'>
print(type(item_quantity))  # <class 'int'>
print(type(item_total_price))  # <class 'int'>

# Each data type is an object that has been instantiated earlier by some class
